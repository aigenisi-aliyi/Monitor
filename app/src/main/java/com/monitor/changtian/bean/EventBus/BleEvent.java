public class BleEvent {
    private String bleStr;
    private BluetoothSocket socket;

    public BleEvent(String bleStr, BluetoothSocket socket) {
        this.bleStr = bleStr;
        this.socket =socket;
    }

    public BluetoothSocket getSocket() {
        return socket;
    }

    public void setSocket(BluetoothSocket socket) {
        this.socket = socket;
    }

    public String getBleStr() {
        return bleStr;
    }

    public void setBleStr(String bleStr) {
        this.bleStr = bleStr;
    }
}
